  alias TimeManager.Task.Workingtime

  def list_workingtimes() do
    Repo.all(Workingtime)
  end
  @doc """
  Returns the list of workingtimes.

  ## Examples

      iex> list_workingtimes()
      [%Workingtime{}, ...]

  """
  def list_specific_workingtimes(user_id, attrs) do
    query = from u in Workingtime,
      where: u.start >= ^attrs["start"] and u.end <= ^attrs["end"] and u.user_id == ^user_id,
      select: u
    Repo.all(query)
  end

